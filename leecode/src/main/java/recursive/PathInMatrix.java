    boolean res = false;
    public boolean exist(char[][] board, String word) {
        //思路 类似岛屿数量
        int m = board.length , n = board[0].length;
        for(int i = 0; i<m; i++) {
            for(int j = 0; j<n;j++) {
                if(board[i][j] == word.charAt(0)) {
                    infect(board, word, i, j, 0);
                    if(res) return res;
                }
            }
        }
        return res;
    }

    public void infect(char[][] board, String word, int i , int j, int k ) {
        if(k == word.length() ) { res = true; return ;}
        if(i<0 || j < 0) return ;
        if(i>= board.length || j>= board[0].length ) return ;
        if(word.charAt(k) != board[i][j]) return;
        //继续往四面方八方找
        char ch = board[i][j] ;
        board[i][j]='0' ;
        infect(board, word, i+1, j, k+1);
        infect(board, word, i-1, j, k+1);
        infect(board, word, i, j+1, k+1);
        infect(board, word, i, j-1, k+1);
        board[i][j]=ch;
    }

